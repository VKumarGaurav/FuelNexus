package com.fuel.nexus.utility;

/**
 * Enum representing types of fuel in the inventory
 */
public enum FuelType {
    // Gaseous fuels
    CNG,        // Compressed Natural Gas
    LPG,        // Liquefied Petroleum Gas

    // Liquid fuels
    PETROL,     // Gasoline
    DIESEL,     // Diesel fuel
    KEROSENE    // Kerosene fuel
}
